public class Assignmentoperator {
    public static void main(String[] args) {
        int a=10;
        int b=5;
        int c=5;
        int d=11;
        int e=13;
        System.out.println(a+=b);
        System.out.println(b-=a);
        System.out.println(c*=8);
        System.out.println(d/=3);
        System.out.println(e%=4);
    }
    
}
